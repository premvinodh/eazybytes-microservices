package com.eazybytes.accounts;

public class Demo {

    public static void main(String[] args) {

    }


    /**
     * This method adds two numbers and returns the sum.
     *
     * @param a the first number
     * @param b the second number
     * @return the sum of the two numbers
     */
    public static int add(int a, int b) {
        return a + b;
    }

    /**
     * This method subtracts b from a and returns the difference.
     *
     * @param a the first number
     * @param b the second number
     * @return the difference of the two numbers
     */
    public static int subtract(int a, int b) {
        return a - b;
    }

    /**
     * This method multiplies two numbers and returns the product.
     *
     * @param a the first number
     * @param b the second number
     * @return the product of the two numbers
     */
    public static int multiply(int a, int b) {
        return a * b;
    }

    /**
     * This method divides a by b and returns the quotient.
     *
     * @param a the first number
     * @param b the second number
     * @return the quotient of the two numbers
     */
    public static int divide(int a, int b) {
        return a / b;
    }
}
